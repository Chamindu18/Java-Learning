// Execute Statements.

public class Lesson_1 {
    public static void main(String[] args) {
        
        // Use 'println' for new line.
        
        System.out.println("I love coding.");
        System.out.println("I'm learning JAVA.");

        // Also can Use '\n' inside a statement for a new line

        System.out.print("I love coding.\n");
        System.out.print("I'm learning JAVA.\n");

        // Use '\t' to add a tab space in a statement.
        
        System.out.println("\tI love coding.");
        System.out.println("I'm learning JAVA.");

        // To surround a statement with "" use \ inside the statement.

        System.out.println("\"I love coding.\"");
        
        // Fast key for println statement = sysout + Ctrl + space

        System.out.println();



    }
} 