public class Lesson_9_Math_Class {
    public static void main(String[] args) {

        // Using Math Class

        double result;

        result = Math.pow(2, 5); // Math.pow = power
        result = Math.abs(-3);   // Math.abs = absolute value
        result = Math.sqrt(16);  // Math.sqrt = square root
        result = Math.round(2.534); // Math.round = rounding to the nearest whole number
        result = Math.ceil(2.33); // Math.ceil = rounding up --> 3
        result = Math.floor(2.33); //  Math.floor = rounding down --> 2
        result = Math.max(10, 20); // Math.max = find the maximum
        result = Math.min(10, 20); // Math.min = find the minimum



        System.out.println(result);
    }
}
